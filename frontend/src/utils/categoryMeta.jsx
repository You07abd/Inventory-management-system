export function getCategoryMeta(category) {
  const fallback = CATEGORY_META[category?.name] ?? DEFAULT_META;
  const Icon = ICON_MAP[category?.icon] ?? fallback.Icon;
  const color = category?.color || fallback.color;

  return {
    ...fallback,
    color,
    bg: category?.color ? `${color}1a` : fallback.bg,
    Icon,
  };
}

export function getCategoryIconKey(category) {
  return category?.icon || CATEGORY_ICON_KEYS[category?.name] || "";
}

export function isUncategorizedCategory(category) {
  return category?.id == null || category?.name === UNCATEGORIZED_CATEGORY.name;
}
